				<h3>Article I: Minimum Wage</h3>
				<h4>Section 1: Wage for Salary Workers</h4>
				<p>The current minimum wage in the United States will be increased by $1.75 so that it becomes $9 an hour. This wage will be attached to inflation so that the wage will increase at the same rate which inflation increases so that the value of the minimum wage today will be worth the same in the future. States are required to have companies in their jurisdiction pay at least the minimum wage to their employees.</p>
				<h4>Section 2: Wage for Tipped Workers</h4>
				<p>The minimum wages for tipped workers, currently $2.13 an hour, will be increased by $2.32 so that it is $4.50 an hour tied to inflation. States are required to have workplaces in which employees receive tips pay at least the tipped workers minimum wage. They are also encouraged, but not required, to pay the minimum wage to tipped workers.</p>
