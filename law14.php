				<h3>Preamble</h3>
				<p>Americans use thousands of plastic and metal drink containers every day. Too many of those containers end up in landfills, when they could be recycled. This bill aims to create a national bottle deposit and refund of 8 cents to help encourage recycling. These regulations will become effective as prescribed in Article II, Section I, section 12010, subsection a.</p>
			</div>
			<div class="section" id="A3">
				<h3>Article I: Findings</h3>
					<h4>Section 1: Congress Finds the Following:</h4>
					<ol>
						<li>The energy required to manufacture beverage containers from recycled containers is often less than the energy required to create new beverage container materials from raw materials.</li>
						<li>Recycling beverage containers would reduce municipal solid waste and reduce the energy and heat-trapping emissions generated in the manufacture of new aluminum, plastics, and other beverage container materials.</li>
						<li>An average of 350,000,000 beverage bottles and cans are sent to landfills, incinerated, or littered every day.</li>
						<li>In 2006, less than half of the 100,000,000,000 aluminum beverage cans purchased were recycled, resulting in the waste of 800,000 tons of aluminum. Nine of ten plastic water bottles, at least 27,000,000,000 bottles a year, end up as garbage or litter, where they take up to 1,000 years to biodegrade.</li>
						<li>A national system for requiring a refund value on the sale of all beverage containers would provide a positive incentive to individuals to clean up the environment, and would result in a high level of reuse and recycling of such containers and help reduce the costs and environmental dangers associated with solid waste management and container manufacturing.</li>
						<li>Many Americans do not have access to recycling programs. States that combine container deposit incentives with convenient redemption locations have beverage container recycling rates ranging from 65 percent to over 90 percent. This is significantly higher than the average recycling rates of 35 percent for aluminum cans, 14 percent for plastic bottles, and 12 percent for glass bottles in States without container deposit systems.</li>
						<li>Many domestic industries are dependent on recycled steel, aluminum, glass and paper. A container deposit system would yield greater access for recyclers to high grade recycling products, preventing these industries from competitive disadvantages in international recycling.</li>
						<li>States with bottle bills have container recycling rates ranging from 60 percent to over 90 percent, compared to the national average recycling rate of 34 percent.</li>
						<li>A national system of beverage container recycling is consistent with the intent of the Solid Waste Disposal Act (42 U.S.C. 6901 et seq.).</li>
					</ol>
			</div>
			<div class="section" id="A4">
				<h3>Article II: Amendment of Solid Waste Disposal Act</h3>
					<h4>Section 1: Amendment.-[sic]</h4>
						<p>The Solid Waste Disposal Act (42 U.S.C. 6901 et seq.) is amended by adding the following new subtitle at the end thereof:</p>
						<h4>Beverage Container Recycling</h4>
						<ol>
							<li>Sec. 12001. Definitions
								<ol type="a">
									<li>For purposes of this subtitle—</li>
									<li>The term beverage means an alcoholic or non-alcoholic, carbonated or noncarbonated liquid that is intended for human consumption.</li>
									<li>The term beverage container means a container constructed of metal, glass, plastic, or some combination of these materials and having a capacity of up to one gallon of liquid and which is or has been sealed and used to contain a beverage for sale in interstate commerce.</li>
									<li>The term beverage distributor means a person who sells or offers for sale in interstate commerce to beverage retailers beverages in beverage containers for resale.</li>
									<li>The term ‘beverage manufacturer’ means a person who manufactures and sells or offers for sale in interstate commerce to beverage distributors or beverage retailers beverages in beverage containers for resale.</li>
									<li>The term ‘beverage retailer’ means a person who purchases from a beverage distributor beverages in beverage containers for sale to a consumer or who sells or offers to sell in commerce beverages in beverage containers to a consumer, but does not include a person who sells or offers to sell the beverages for consumption on the premises.</li>
									<li>The term ‘deposit initiator’ means a person who is a beverage manufacturer, beverage importer or beverage distributor, the domestic beverage manufacturer or the first domestic entity selling the beverage to the retailer.</li>
									<li>The term consumer means a person who purchases a beverage container for any use other than resale.</li>
									<li>The term refund value means the amount specified as the refund value of a beverage container under section 12002.</li>
									<li>The term ‘recovery for beverage containers’ includes both beverage containers and other containers that are made of beverage container materials.</li>
								</ol>
							</li>
							<li>Sec. 12002. Required beverage contained labeling
								<ol type="a"><li>Except as otherwise provided in section 12007, no beverage distributor or beverage retailer may sell or offer for sale in interstate commerce a beverage in a beverage container unless there is clearly, prominently, and securely affixed to, or printed on, the container a statement of the refund value of the container in the amount of 8 cents. The Administrator shall promulgate rules establishing uniform standards for the size and location of the refund value statement on beverage containers. The 10-cent amount specified in this section shall be subject to adjustment by the Administrator as provided in section 12008.</li></ol>
							</li>
							<li>Sec. 12003. Origination of refund value
								<ol type="a"><li>For each beverage in a beverage container sold in interstate commerce to a beverage retailer by a deposit initiator, the distributor shall collect from the retailer the amount of the refund value shown on the container. With respect to each beverage in a beverage container sold in interstate commerce to a consumer by a beverage retailer, the retailer shall collect from the consumer the amount of the refund value shown on the container. No person other than the persons described in this section may collect a deposit on a beverage container.</li></ol>
							</li>
							<li>Sec. 12004. Return of Refund Value
								<ol type="a"><li>Payment by Retailer -
									<ol type="i">
										<li>In general, except as provided in paragraph (2), if any person tenders for refund an empty and unbroken beverage container to a beverage retailer who sells (or has sold at any time during the period of 3 months ending on the date of such tender) the same brand of beverage in the same kind and size of container, the retailer shall promptly pay such person the amount of the refund value stated on the container.</li>
										<li>Exceptions - A retailer shall not be required to accept tender of a beverage container from any person under paragraph (1)—
											<ol>
												<li>if the beverage container contains or is contaminated by a hazardous waste;</li>
												<li>in excess of 600 individual beverage containers per day if the retailer occupies a space less than 5,000 square feet; or</li>
												<li>in excess of 1,800 individual beverage containers per day if the retailer occupies a space greater than 5,000 square feet.</li>
											</ol>
										</li>
									</ol>
								</li>
								<li>Payment by Distributor - If any person tenders for refund an empty and unbroken beverage container to a beverage distributor who sells (or has sold at any time during the period of 3 months ending on the date of such tender) the same brand of beverage in the same kind and size of container, the distributor shall promptly pay such person the amount of the refund value stated on the container (8 cents). This subsection shall not preclude any person from tendering beverage containers to persons other than beverage distributors.</li>
								<li>Agreements -
									<ol type="i">
										<li>Nothing in this subtitle shall preclude agreements between distributors, retailers, or other persons to establish centralized or co-located beverage collection centers, including centers which act as agents of such retailers.</li>
										<li>Nothing in this subtitle shall preclude agreements between beverage retailers, beverage distributors, or other persons for the crushing or bundling (or both) of beverage containers.</li>
									</ol>
								</li>
								<li>Broken containers - The opening of a beverage container in a manner in which it was not designed to be opened and the compression of a beverage container made of metal or plastic shall not, for purposes of this section, constitute the breaking of the container if the statement of the amount of the refund value of the container is still readable.</li>
								<li>Reports - Deposit initiators shall provide annual reports to the Administrator or designated State agency documenting their rate of redemption.</li>
							</ol>
							</li>
							<li>Sec. 12005. Accounting for unclaimed refunds and provisions for State recycling funds
								<ol type="a">
									<li>Unclaimed Refunds - At the end of each fiscal quarter, each deposit initiator shall pay to each State an amount equal to the sum by which the total refund value of all containers sold by the deposit initiator for resale in that State during that year exceeds the total sum paid during that period by the initiator under section 12004(b) to persons in that State. The total of unclaimed refunds received by any State under this section shall be available to carry out programs designed to reduce greenhouse gas emissions within the State, including but not limited to State and local recycling programs.</li>
									<li>Refunds in Excess of Collections - If the total of payments made by a deposit initiator in any calendar year under section 12004(b) for any State exceed the total refund value of all containers sold by the initiator for resale in that State, the excess shall be credited against the amount otherwise required to be paid by the initiator to that State under subsection (a) for a subsequent fiscal year quarter designated by the deposit initiator.</li>
								</ol>
							</li>
							<li>Sec. 12006. Prohibitions on disposal
								<ol type="a"><li>No retailer or distributor or agent of a retailer or distributor may intentionally dispose of any beverage container labeled under section 12002 or any metal, glass, or plastic from such a beverage container (other than the top or other seal thereof) in any landfill or other solid waste disposal facility.</li></ol>								
							</li>
							<li>Sec. 12008. Regulations
								<ol type="a">
									<li>Not later than 12 months after the date of enactment of this subtitle, the Administrator shall prescribe regulations to carry out this subtitle. The regulations shall include a definition of the term beverage retailer in a case in which beverages in beverage containers are sold to consumers through beverage vending machines. Such regulations shall also adjust the 10-cent amount specified in section 12002 and the 2-cent amount specified in section 12004 to account for inflation. Such adjustment shall be effective 10 years after the date of enactment of this subtitle and additional adjustments shall take effect at 10-year intervals thereafter. The regulations shall also permit the Administrator to increase such amounts by an additional amount after the expiration of 5 years after the date of enactment of this subtitle.</li>
								</ol>
							</li>
							<li>Sec. 12009. Penalties
								<ol type="a">
									<li>Any person who violates any provision of section 12002, 12003, 12004, or 12006 shall be subject to a civil penalty of not more than $1,000 for each violation. Any person who violates any provision of section 12005 shall be subject to a civil penalty of not more than $10,000 for each violation.</li>
								</ol>
							</li>
							<li>Sec. 12010. Effective date
								<ol type="a">
									<li>Except as provided in section 12008, this subtitle shall take effect 2 years after the date of its enactment.</li>
								</ol>
							</li>
						</ol>
