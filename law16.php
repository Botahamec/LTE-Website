			<div class="section" id="A2">
				<h3>Preamble</h3>
				<p>From Congressional Bill #018, we see that children will no longer be separated from their parents except in cases of abuse or neglect. But what will happen to these families? Congressional Bill #018 creates a plan for children currently in these detainment centers and for future families that illegally cross the border.</p>
			</div>
			<div class="section" id="A3">
				<h3>Article I: Deportation vs. Permanent Entry</h3>
				<h4>Section 1: Minors Currently in American Detainment Centers</h4>
				<p>Minors currently in American Detainment Centers that are separated from their parents will be attempted to be met up with their parents as soon as possible. If the attempt is failed, minors  will be met up with anyone they know, such as friends or extended family already in the United States of America or, if possible, in their home country. If that is not an option, children will be placed into foster care where they will hopefully find a forever home.</p>
				<h4>Section 2: Undocumented Unaccompanied Minors</h4>
				<p>Minors found crossing the border alone will be placed into a temporary detainment center or tent camp for no more than four weeks until the child can be placed with family, friends, or non-related, willing sponsors. When placed in the United States, the child will have the option to have permanent residence in the United States. If the child would like to go back to their home country, or immediate family of the child is found in their home country, they will be permitted to return. However, if the child wishes to stay in the United States and/or no family is found, the child will be granted permanent residence in the United States.</p>
				<h4>Section 3: Undocumented Adults and Families Without Minors</h4>
				<p>Undocumented immigrants above the age of seventeen will be placed into a detainment center or tent camp for no more than six weeks. From this camp, these immigrants will be assisted back to their home country. Food and water as well as protected travel will be provided to these immigrants. These immigrants will not be granted permanent entry into the country unless a court hearing has decided in favor of permanent entry.</p>
				<h4>Section 4: Undocumented Families With Minors</h4>
				<p>Undocumented immigrant families with minors  will be placed into detainment centers or tent camps for no more than four weeks. From their camp, these immigrants will be assisted back to their home country. Food and water as well as protected travel will be provided to these immigrants. These families  will not be granted permanent entry into the country unless a court hearing has decided in favor of permanent entry.</p>
				<h4>Section 5: Undocumented Special Needs Adults</h4>
				<p>Undocumented immigrants above the age of seventeen with life-threatening or brain disabilities will be placed into special needs designated detainment centers or tent camps  where they will be given extra assistance and care. These immigrants will be well cared for and will be permitted to stay in these camps for no more than four weeks. From their camp, these immigrants will be assisted back to their home country. Food and water as well as protected travel will be provided to these immigrants. These families will not be granted permanent entry into the country unless a court hearing has decided in favor of permanent entry.</p>
			</div>
			<div class="section" id="A4">
				<h3>Article II: Sponsors and Foster Care</h3>
				<h4>Section 1: Definition of Sponsor</h4>
				<p>A sponsor, in this case, is someone who will provide a home, temporary or permanent, to an undocumented immigrant. This/these sponsor(s) must have a clean criminal background(s) and must be first approved by the government of the United States.</p>
				<h4>Section 2: Foster Care</h4>
				<p>An undocumented immigrant child may also be placed into the United States foster care system, where they will move around homes until finding their permanent home.</p>
				<h4>Section 3: Safe Homes Only</h4>
				<p>Before an undocumented immigrant is placed into a home, the home must have gone through an inspection process and everyone currently residing in the home must have a clean criminal background. Biometric data, such as fingerprints, will also be required from the main sponsor(s). Home inspections will be given every four to six months while a minor is currently residing in the home or the home wishes to remain a sponsor home for future immigrants.</p>
			</div>
			<div class="section" id="A5">
				<h3>Article III: Detainment Centers and Tent Camps</h3>
				<h4>Section 1: Clean and Safe</h4>
				<p>All detainment centers and tent camps are to remain clean and daily health inspections must be given to all camps. Food and water are to be provided to all immigrants currently residing in a camp.</p>
				<h4>Section 2: Healthcare</h4>
				<p>Assistance is to be provided to all sick and/or injured immigrants residing in a camp. Immigrants must be able to get reasonable health assistance during their time in a detainment/tent camp.</p>
				<h4>Section 3: "Reasonable Health Assistance</h4>
				<p>“Reasonable Health Assistance” shall be defined as access to a medical center with trained doctors to aid with disease and other health issues. Condoms and feminine products shall also be available at all times.</p>
