			<div class="section" id="A2">
				<h3>Preamble</h3>
				<p>Small businesses have been, and will continue to be, absolutely crucial to the health and well-being of our economy. For an economy to thrive and remain prosperous, small businesses must be accommodated for and allowed to succeed, due to the fact that our economy hinges on the ability of these small businesses to continue hiring and providing products and services to us. The benefits and importance of small businesses include, but are not limited to:</p>
				<ul>
					<li>Jobs: Since 1995, small businesses have generated 64 percent of new jobs, and paid 44 percent of the total United States private payroll, according to the SBA.</li>
					<li>Economic Contribution: Small businesses generate about 50 percent of our gross domestic product (GDP).</li>
					<li>Innovation: During a recent four-year period, large firms generated 1.7 patents per hundred employees, whereas small firms generated an impressive 26.5 patents per hundred employees.</li>
					<li>Contribution to larger businesses: Small firms supply many of the components needed by larger companies.</li>
				</ul>
				<p>Due to the culmination of both the sheer economic importance of small businesses and the vulnerability they may have within our current business environment, this bill seeks to provide assistance, in the form of tax credits, to businesses who are deserving and are in need. This bill will be placed into effect the fiscal year directly following its passage.</p>
			</div>
			<div class="section" id="A3">
				<h3>Article I: Creation of the Small Business Assistance Program</h3>
				<h4>Section 1: Initiation of the Small Business Assistance Program</h4>
					<p>As of this bills signing, a new program, administered by the SBA (as outlined in Article II), titled the “Small Business Assistance Program”, will be set into effect the next fiscal year.  The scope of and effect of this program will be outlined in the following sections of this article.</p>
					<p>(Please note that the term “SBA” refers to the Small Business Administration)</p>
				<h4>Section 2: Effect of the Small Business Assistance program</h4>
					<p>The Small Business Assistance Program will grant each qualifying small business a tax credit worth approximately 5-15 percent of the small business’s respective income. In the discretion of the Small Business Association, businesses with higher degrees of need will be given more significant tax credits, whereas those with lesser degrees of need will be given less significant tax credits.</p>
				<h4>Section 3: Qualifications for the Small Business Assistance Program</h4>
				<p>In order to qualify for the Small Business Assistance Program, prospective businesses must be:</p>
				<ul>
					<li>Severely struggling financially (profit of $30,000 or less, or a profit margin of 4% or lower )</li>
				</ul>
				<p>And may be accepted if one of the following statements is true:</p>
				<ul>
					<li>They have displayed signs of a strong potential to innovate and have competent leadership</li>
					<li>They have hired at least 12 employees, possess competent leadership, and have the ability, in the discretion of the SBA, to grow and contribute to the economy if given assistance</li>
					<li>They have become, in the discretion of the SBA, crucial to the local economy and culture and possess competent leadership.</li>
				</ul>
			</div>
			<div class="section" id="A4">
				<h3>Article II: Administration of the Small Business Assistance Program</h3>
				<h4>Section 1: Application Process</h4>
				<p>To apply for the Small Business Assistance Program, prospective businesses must first complete an Application online (provided by the SBA) that requires information regarding the identity of the business itself to be forwarded to the SBA. Once the SBA has confirmed the identity of the business, the business owner will be asked to inform the SBA of why they believe they’re deserving of the assistance, and will be required to send all financial information, employment information, patent information, and any other information the SBA requests or the owner chooses to send. If the applicant passes this step, they’ll be required to visit the nearest SBA office to participate in an interview with an SBA official. If the SBA deems the business worthy based on the qualifications outlined in Article I, they’ll be accepted and will be granted all the benefits outlined in Article I.</p>
				<h4>Section 2: Annual Review</h4>
				<p>At the end of every year a Small Business receives assistance from the Small Business Assistance Program, they’ll be subjected to a process known as “Annual Review” in which their financial situation, leadership, and potential to grow and innovate is reevaluated. If the company is no longer struggling financially, the leadership is found to be poor, or the SBA’s judgment on any other factor that contributed to their acceptance to the program is overturned, the business will no longer be able to reap the benefits of the Small Business Assistance Program. Otherwise, the business will still be given access to the same benefits and assistance as before.</p>
