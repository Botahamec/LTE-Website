			<div class="section" id="A2">
				<h3>Preamble</h3>
				<p>Due to potential absences of Senators or Representatives, it is important to allow fill-ins. In the case of abstinence, a congressman or congresswoman may have a replacement fill in for them on that particular vote or for a prolonged period of time. This fill-in must be approved by the leader of the congresspersons respective house. These Emergency fill-ins will go by EFI for the majority of this bill. And a SEFI(special emergency fill in) may be appointed to a Representative when the Speaker of the house deems it necessary.</p>
			</div>
			<div class="section" id="A3">
				<h3>Article I: Senate Fill Ins</h3>
					<h4>Section 1: Max Duration</h4>
						<p>In the case of an emergency fill-in (EFI) the senator may not appoint the EFI to their role for more than a 72 hour period. If the EFI is to stay in that role for more than the allotted 72 hours, they will lose all voting privileges and the senator the EFI is filling in for will be counted as abstinent. </p>
					<h4>Section 2: Appointment</h4>
						<p>In order for an EFI to be appointed to fill in for a senator, the senator must suggest the EFI to the Senate majority leader and the Senate majority leader must confirm. The Senate majority leader may also suggest an EFI for a senator but the senator must consent to the EFI. </p>
					<h4>Section 3: Dismissal of an EFI</h4>
						<p>An EFI can be dismissed by the Senate Majority leader or the senator that the EFI is appointed to at any time. This dismal is to be made in the Senate chamber and is effective and soon as the Senate majority leader or senator that the EFI is appointed too declares it. An EFI may not dismiss themselves but may petition the Senate Majority leader to do so.</p>
					<h4>Section 4: Job</h4>
						<p>The EFI may vote on any proposed bills but may not propose any bills to the floor during their time as an EFI. An EFI may go against the senator they are appointed to wishes but are discouraged from doing so and may be dismissed for doing such.</p>
					<h4>Section 5: Cooldown</h4>
						<p>A Senator may not appoint an EFI for themselves if any EFI has been diminished or appointed in the last 72 hours.</p>
			</div>
			<div class="section" id="A4">
				<h3>Article II: House of Representatives</h3>
					<h4>Section 1: Max Duration</h4>
						<p>In the case of an emergency fill-in (EFI) the Representative or Speaker of the house may not appoint the EFI to their role for more than a 48 hour period. If the EFI is to stay in that role for more than the allotted 48 hours, they will lose all voting privileges and the senator the EFI is filling in for will be counted as abstinent.</p>
					<h4>Section 2: Attorney General Approval</h4>
						<p>In order for an EFI to be appointed to fill in for a Representative, the  Representative must suggest the EFI to the Speaker of the house and the speaker of the house must confirm. The speaker of the house may also force appoint an EFI to a Representative if 2/3rds of the House votes to affirm it. This Special EFI (SEFI) may not serve for more than 12 hours. </p>
					<h4>Section 3: Dismissal of an EFI</h4>
						<p>An EFI can be dismissed by the Speaker of the House or the Representative that the EFI is appointed to at any time. This dismal is to be made in the House chamber and is effective and soon as the Speaker of the house declares it or the Representative that the EFI is appointed to declares it. An EFI may not dismiss themselves but may petition the Speaker of the house to do so.</p>
					<h4>Section 4: Job</h4>
						<p>The EFI may vote on any proposed bills but may not propose any bills to the floor during their time as an EFI. An EFI may go against the Representative they are appointed to wishes but are discouraged from doing so and may be dismissed for doing such.  The same applies to a SEFI.</p>
					<h4>Section 5: Dismissal of a SEFI</h4>
						<p>The SEFI can not be dismissed once appointed except in the case of a 2/3rds vote of the house to do so. This vote can be initiated by anyone including the SEFI.</p>
					<h4>Section 6: Cooldown for EFI's and SEFI's</h4>
						<p>A Representative may not appoint an EFI for themselves if any EFI has been diminished or appointed in the last 72 hours. There are no cooldowns for SEFIâ€™S</p>
