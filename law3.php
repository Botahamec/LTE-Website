				<h3>Preamble</h3>
				<p>As the country becomes increasingly populated with more registered voters, uncertainties apropos the election process have appeared. The Election Initiation and Process Act is designated to clarify these uncertainties and gray areas. This act also details the specifics on who can run, when can run, where they can run and how candidates can run. Furthermore, this bill details the special election process, as a region will hold a special election when the registered voter quota is reached.</p>
			</div>
			<div class="section" id="A3">
				<h3>Article I: General and Congressional Elections</h3>
					<h4>Section 1: Initiation of Elections</h4>
						<p>The initiation of The Election Process will commence as soon as the Secretary of State and decides so, but they must meet certain requirements.</p>
						<ol>
							<li>The Elections must be held the Saturday before the 22nd of each month  (due to Congressional Inauguration being on the 22nd).</li>
							<li>On Presidential Election Months, the election process can start as early as 15 days from the time and as late as 11 Days from the date specified in Requirement #1.</li>
							<li>In the case of it being a Midterm Election Month, the process can start as early as 12 days and as late as 8 days from the date specified in Requirement #1.</li>
						</ol>
					<h4>Section 2: Primary Registration and Process</h4>
						<p>Within the span of our elections, primaries will exist. These will dictate the respective party’s nominee for that said race. We have to make sure these are fair, and done in a professional matter.</p>
						<p>If a candidate wishes to participate in a primary election, all they have to do is meet the requirements of the Constitution, be a registered voter,  and announce their candidacy in their designated region and in the designated channel. The time frame to announce campaigns will be set by the Secretary of State.</p>
						<p>In the instance of Presidential Primaries, each region’s primary will take place on a separate day, spanning 6 days in total. The accumulated popular vote from all of the regions will decide the Presidential Nominee for that Party.</p>
						<p>For congressional primaries, they will take place on the same day as the Presidential Primaries in that region on Presidential Election Months, but in midterm months, will take place on the same day for all regions.</p>
						<p>Incase a candidate wins a primary but drops out before Election Day, the specified political party can either, A.) Replace said candidate with no primary, or B.) Replace said candidate with a primary before Election Day.</p>
						<p>Independent Candidates are exempt from party registration dates, but must comply with the time frame given by The Secretary of State.</p>
					<h4>Section 3: Multiple House Seat Clause</h4>
						<p>In the instance that a region has multiple house seats, the candidates might not have to go through a primary, but will participate in a “jungle election” on Election Day. Political parties also hold the power to have primaries in these “Jungle” Races if necessary if there’s a surplus of candidates, but this has to be approved by the party leaders first. The top finishers getting seats in Jungle Races, (ex. If there are three seats available and there’s four candidates, the top three get a seat.) The amount of seats up in the general election will be dictated 3-5 days before the election by the Secretary of State.</p>
						<p>However, in case a region gets more than 5 seats, each race will be separated into its own. The political parties participating in the Region’s Elections will select who will go into each race (The Secretary of State can change who is against who if necessary), and if there is a surplus of candidates, the political party can either hold a “jungle” primary (described above), or pick two persons to go against each other in a primary. </p>
						<p>As the Constitution states, “A representative is awarded per every 8 registered voters in a state,” (which starts giving extra seats at 16 registered voters + one extra incase an individual leaves)), if a region goes under the amount required to hold the amount of seats the day that candidacy filing ends in the region either in a general or midterm election, the seats up for election  will decrease. But the seat will not be removed until after the next inauguration, (22nd of each month).</p>
					<h4>Section 4: Barred from Running</h4>
						<p>If an individual breaks the law, they can be barred from running a campaign, but this is only in an instance of illegal campaign practices.</p>
						<p>This said punishment can only be enforced by either The Staff Team or Attorney General, if applicable by law.</p>
						<p>If a candidate violates their restriction on running, their campaign will be immediately suspended by the Attorney General, and increased punishment may be warranted.</p>
						<p>If a candidate violates any laws that affect the election process, their campaign can be immediately suspended without notice by The Attorney General or The Staff Team.</p>
					<h4>Section 5: Election Day</h4>
						<p>All candidates that make it past the primary and are still in the race on the Saturday before the 22nd of the month will be in the General Election, during this, registered voters  will vote and pick the winners in the specified region.</p>
						<p>The ballots for these elections will be created by the Staff Team, who will also count the votes. No person will be allowed more than one vote.  After the votes are counted, The Secretary of State will verify the outcome of each election. </p>
						<p>All questions on the ballots have to be marked as “Required”, to create higher voting turnout per election category. Under no circumstances can that be changed to compensate for an individual’s unwillingness to vote.</p>
						<p>Only presidential election ballots require a “Write-In” option, which allow people to vote for whoever they want. Congressional ballots do not based on the fact that there are not many voters in a single region.</p>
						<p>If a candidate wishes to have a recount, they may ask The Secretary of State, and it is up to them to decide if a recount should be needed or not, (this can only be done if the margin of victory is &#x3c;10%.)</p>
						<p>Note: On Election Day, Voter Registration will be closed.</p>
					<h4>Section 6: Inauguration After Elections</h4>
						<p>The scheduled days for Inaugurations go as followed:</p>
						<p>House of Representatives - 22nd of Every Month</p>
						<p>Senate - 22nd of Every other Month</p>
						<p>President / Vice President - 24th of Every other Month</p>
