			<div class="section">
				<h3>Article II: Special Elections Process</h3>
					<h4>Section 1: Timeline</h4>
						<p>After the population of the United States began to grow exponentially, the Congress believes that instead of a redistricting every decade, we will hold a special election after the state receives enough registered voters, or a representative / senator resigns.</p>
						<p>When this occurs, The Secretary of State can initiate a special election, which will last 5 Days. During this, the following with go on:</p>
						<p><b>Days 1 &amp; 2:</b> Candidacy Filing Period</p>
						<p><b>Day 3:</b> Party Primaries</p>
						<p><b>Day 4:</b> Campaign Day &amp; Last Day for Independents to Announce Candidacies.</p>
						<p><b>Day 5:</b> The Special Election</p>
						<p>But, The Secretary of State cannot initiate a Special Election after the 6th of the Month, and a region has to have one more registered voter than required for an extra seat in case of an individual leaving the LTE Community. If the amount of the registered voters goes under the required, the special election will be cancelled.</p>
						<p>Even if the region is within the requirements of General or Midterm Elections going by “Jungle” Elections, (described in Article I, Section III) the special election will not go by that method, and will go by the same method used for regions either under or above the requirements for “Jungle” Elections.</p>
					<h4>Section 2: The Protocol in the Absence of Candidates</h4>
						<p>If there are no candidates that would like to run in the special election by the end of the filing period, the special election will be postponed to the upcoming general or midterm election.</p>
						<p>The Secretary of State can extend Candidacy Filing by 1 day if completely necessary.</p>
					<h4>Section 3: Standards</h4>
						<p>The Election Day for special elections will go by what is specified in Article I, Section V for normal election days, and the same principles from Article I, Section IV are still in effect in Special Elections.</p>
						<p>Voter Registrations for the region having a special election will not be able to be registered during the Special Election Day and will have to wait until the day after.</p>
