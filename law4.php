			<div class="section" id="A2">
				<h3>Preamble</h3>
					<p>A bill to improve our nation's healthcare system. Done through increased funding, and reform of aspects of our medical system.</p>
			</div>			
			<div class="section" id="A3">
				<h3>Article I: Creation of a Department of Health and Human Services</h3>
					<h4>Section 1</h4>
						<p>Mandates for the creation of a cabinet level post does not have to be appointed in the LTE server universe, may change with the objective to grow and protect the level of healthcare and social program help that citizens of our country recieve.</p>
					<h4>Section 2</h4>
						<p>Funds for this cabinet level department will be set out by congress each year, with funds able to be temporary, recurring, mandatory or a one time payment. Funds can also be a mix of these 4 options. Article comes into effect immediately after signature of president.</p>
					<h4>Section 3</h4>
						<p>All Healthcare related bills passed by congress, and signed by the president, are under the oversight of the Secretary of the Department of Health and Human Services, and the President.</p>
					<h4>Section 4: Department Seal</h4>
						<img src="https://i.imgur.com/NepM2QG.png" height="144pt">
			</div>
			<div class="section" id="A4">
				<h3>Article II: Funding for Underprivileged and Local Hospitals and Medical Centers, + Health Care Wages</h3>
					<h4>Section 1</h4>
						<p>Increases federal funds for total and exact for article, see Section VII of  for underprivileged, dilapidated and/or local hospitals for the use of improving structural elements. Funds shall be allocated to hospital/medical center by the Department of Health and Human Services. </p>
					<h4>Section 2</h4>
						<p>Funds shall be used for the repairing of hospitals/medical centres, in areas such as walls, windows, roofs, floors, heating/cooling systems, exterior appearance, redevelopment of wards and specialty areas such as surgical rooms, waiting rooms, overhaul of medical databases, medical database and hospital security, the development and increased incorporation of technology and the improvement and updating of computer systems.</p>
					<h4>Section 3</h4>
						<p>Funds shall be used for all hospitals in this country, but private hospitals only receive funds immediately if an emergency concerning them occurs.</p>
					<h4>Section 4</h4>
						<p>Funds may also be used for the allocation of HHS inspectors to determine whether a hospital able to receive funds may have to be rebuilt at an alternate site. This may be due to irreparable damage, which will be determined by such inspectors.</p>
					<h4>Section 5</h4>
						<p>Funds shall also be used for the increase of hospital worker wages, this may be done as a monetary bonus on a paycheck, or to save funds for an overall increase in the rate of wage growth. Dept. of HHS is responsible for the working out of wage increases, and will act in mind to prioritise underpaid workers and certain professions in need of a pay increase.</p>
					<h4>Section 6</h4>
						<p>Article comes into effect immediately after signature of president and incorporation of economic simulator.</p>
					<h4>Section 7</h4>
						<p>Total article funds are a total payment of 250 billion dollars over 5 years (50 Billion/year). This will be given out with Dept. of HHS authority. If total after 5 years is not exactly 250 billion, either the extra funds spent will be taken out of HHS next budget, or extra congressional funds will be spent.</p>
			</div>
			<div class="section" id="A5">
				<h3>Article III: Funding for Medical Equipment Purchase</h3>
					<h4>Section 1</h4>
						<p>Funds for the purchase of equipment needed for Underprivileged/local hospitals in need. Medical equipment needed will be approved by the Dept. of Health and Human Services.</p>
					<h4>Section 2</h4>
						<p>Medical Equipment shall be defined as, and nothing else, than ‘something that is designed to aid in the diagnosis, monitoring or treatment of medical conditions, in hospitals or medical clinics’.</p>
					<h4>Section 3</h4>
						<p>The Dept. of HHS is allowed to take penal punishment against hospitals, appropriators, or states that misuse what funds are allowed to be spent on.</p>
					<h4>Section 4</h4>
						<p>Total article funds are 30 billion dollars spread over 5 years (6 billion/year).</p>
					<h4>Section 5</h4>
						<P>Article comes into effect immediately after signature of president and incorporation of economic simulator.</P>
			</div>
			<div class="section" id="A6">
				<h3>Article IV: Classification Regarding Hospitals and Medical Centers by the Dept. of HHS</h3>
					<h4>Section 1</h4>
						<p>Funds recurring annual payment of 35 million dollars for the establishment of a Dept. of Health and Human Services agency, which classifies hospitals on a variety of factors on different scales. These scales will be measuring the hospital capacity used, the individual wings of a hospital, maintenance/structurability, quality of service and being up to date with modern hospital technology and practises.</p>
					<h4>Section 2</h4>
						<p>The funding of a recurring per annum payment to the annual budget of the Dept. of HHS, that goes to the establishment and operating of this agency, by congress.</p>
					<h4>Section 3</h4>
						<p>Capacity used is defined as the capacity of patients that the hospital or medical center is able to have at any given moment. The measuring will be determined by how much as a percentage is the capacity used over a month for each healthcare facility.</p>
					<h4>Section 4</h4>
						<p>Scales measuring the individual wings of a hospital will have a rating for each hospital wing. (eg: CT dept. may have a different rating to a Radiography Dept. within a hospital.)</p>
					<h4>Section 5</h4>
						<p>Article comes into effect immediately after signature of president and incorporation of economic simulator.</p>
			</div>
			<div class="section" id="A7">
				<h3>Article V: Expansions to Near or Full Capacity Hospitals/Medical Centers</h3>
					<h4>Section 1</h4>
						<p>Allocates funds to be spent structurally expanding hospitals or medical centers which are rated as High, or Very High on the capacity used scale rating by the HHS [see Article III, Section I and Section III].</p>
					<h4>Section 2</h4>
						<p>Allocates funds for the expansion and/or redevelopment of individual hospital wings rated as a low, poor or very poor rating by the DHS scale on hospital wings [See Article III, Section I and Section IV].</p>
					<h4>Section 3</h4>
						<p>Total article funds are a total payment of 3 billion dollars over 3 years.</p>
					<h4>Section 4</h4>
						<p>Article comes into effect immediately after signature of president and incorporation of economic simulator.</p>
			</div>
			<div class="section" id="A8">
				<h3>Article VI: Increased Funds For Healthcare Awareness Programs</h3>
					<h4>Section 1</h4>
						<p>Allocates funds to the annual budget of the Dept. of Health and Human Services to increase advertising and general awareness of Healthcare issues.</p>
					<h4>Section 2</h4>
						<p>Funds may be used for increased advertising, such as television, radio, posters or billboards to increase such awareness.</p>
					<h4>Section 3</h4>
						<p>Healthcare issues is defined as ‘an issue, topic or illness that is related to the topic of general health’.</p>
					<h4>Section 4</h4>
						<p>An example of a healthcare issue may be awareness about the symptoms/signs, treatment and maybe busting a common misconception about a disease, the simple actions in life we can do to protect our own health, and the ways you can help you local health system through charitable work or donation. [examples do not define what funds must be spent on, intention was to give an idea of the ways appropriated funds may be used].</p>
					<h4>Section 5</h4>
						<p>Article comes into effect immediately after signature of president and incorporation of economic simulator.</p>
					<h4>Section 6</h4>
						<p>Total article funds are a recurring annual payment of 400 million dollars.</p>
			</div>
			<div class="section" id="A9">
				<h3>Article VII: Increased Funds for Mental Illness Programs</h3>
					<h4>Section 1</h4>
						<p>Establishes federal government funds to go towards the funding of Department of Health and Human Services Mental Illness programs that help the people with such illnesses, and contribute to medical research to help cure (some- (some are un-curable with current technology)) of these diseases</p>
					<h4>Section 2</h4>
						<p>Funds act as an added contribution to existing Dept. of HHS Mental Illness programs from this congress, and are a recurring payment. (Finds existing Dept. of HHS mental illness programs suitable).</p>
					<h4>Section 3</h4>
						<p>Research funds from congress are handed out by the Dept. of HHS, the Department of Commerce and Department of Education,in cooperation with another. This is so research and research funds conducted within all relevant departments are working together.</p>
					<h4>Section 4</h4>
						<p>Article comes into effect immediately after signature of president and incorporation of economic simulator.</p>
					<h4>Section 5</h4>
						<p>Total article funds are a recurring payment of 240 million dollars.</p>
			</div>
			<div class="section" id="A10">
				<h3>Article VIII: Lowering Prescription Drug Prices through a Pharmaceutical Benefits Scheme</h3>
					<h4>Section 1</h4>
						<p>Allocates congressional funds for the Department of Health and Human Services to create a Pharmaceutical Benefits Scheme (PBS), which is responsible for subsidising Pharmaceutical companies, in return for a lowering of the cost of a particular medicine.</p>
					<h4>Section 2</h4>
						<p>The PBS will subsidise for specific drugs, which will chosen by the Dept. of HHS. The Public can petition the Dept. of HHS to add a drug to the PBS program.</p>
					<h4>Section 3</h4>
						<p>The subsidised funds given to Pharma companies in return for a lowering of drug prices will be negotiated on the behalf of this congress by HHS officials.</p>
					<h4>Section 4</h4>
						<p>Article of Bill also stops the sale of doctors selling medicinal drugs, due to the past likelihood of doctors marking up prices unnecessarily and unfairly.</p>
					<h4>Section 5</h4>
						<p>Article also prohibits the use of medication marketing used by pharmaceutical companies to advertise medicines/drugs. Article funds are undetermined at the time of bill writing, as they are the total of all current government payments to pharmaceutical companies to reduce drug prices for that year.</p>
					<h4>Section 6</h4>
						<p>Article comes into effect immediately after signature of president and incorporation of economic simulator.</p>
			</div>
			<div class="section" id="A11">
				<h3>Article IX: Bill/Act Funding</h3>
					<h4>Section 1</h4>
						<p>Funding for this act will be funded by a mix of private and public (government) funds.</p>
					<h4>Section 2</h4>
						<p>Article I and Article IV will be majority privately funded, with the dept. of HHS giving out government contracts for projects in those respective articles. Companies will receive a tax credit for funding such projects, with companies outlined and given such credits by the Dept. of HHS.</p>
					<h4>Section 3</h4>
						<p>Public funds will come from a 5 year temporary tax increase of 2.75% on individual annual income over $200,000, 0.95% on income from $82,501 to $157,500 and 0.45% on individual income below $82,500. (in a reverse progressive tax rate system).
					<h4>Section 4</h4>
						<p>Temporary tax 5 year tax increases are 3% for joint income over $400,001, 1.25% for joint income from 165,001 to $400,000, 0.8% for joint income from $77,401 to $165,000, and joint income below $77,400 is free from temporary tax increases.</p>
					<h4>Section 5</h4>
						<p>Of all tax increases temporary or permanent, a 1% tax increase on income over $157,501, and 2% on married joint filers with incomes over 315,001 to $400,000, and will be permanent to pay for recurring permanent payments for some articles and sections.</p>
					<h4>Section 6</h4>
						<p>If extra funds are raised than spent, surplus funds will be saved in a Dept. of HHS pool, to be saved for future healthcare related use.</p>
					<h4>Section 7</h4>
						<p>Article comes into effect immediately after signature of president and incorporation of economic simulator.</p>
