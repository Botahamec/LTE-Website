				<h3>Preamble</h3>
				<p>This bill aims to fully protect and grant legal status to “dreamers” or deferred action recipients. This bill shall immediately go into effect, at the bill’s passing</p>
			</div>
			<div class="section" id="A3">
				<h3>Article I: Who are Dreamers?</h3>
					<h4>Section 1: Dreamer Identification</h4>
						<p>As detailed in the original DACA “dreamer” definition, in order to be a dreamer, one:</p>
						<ol>
							<li>must have been continuously physically present in the United States for four years preceding this bill's enactment;</li>
							<li>was younger than 18 years of age on the initial date of U.S. entry;</li>
							<li>is not inadmissible on criminal, security, terrorism, or other grounds;</li>
							<li>has not participated in persecution;</li>
							<li>has not been convicted of specified federal or state offenses; and</li>
							<li>has fulfilled specified educational requirements.</li>
						</ol>
					<h4>Section 2: Continuous Presence Clarification</h4>
						<P>Any period of continuous physical presence in the United States of an undocumented immigrant who applies for permanent resident status on a conditional basis under this section shall not terminate when the undocumented immigrant is served a notice to appear under section 239(a) of the Immigration and Nationality Act (8 U.S.C. 1229(a)). Any specifics regarding documentation shall be determined by DHS officials, with guidance from Congress.</P>
			</div>
			<div class="section" id="A4">
				<h3>Article II: Documentation Requirements</h3>
					<h4>Section 1: Identity Documentation</h4>
						<p>Documents Establishing Identity - An undocumented immigrant’s application for permanent resident status on a conditional basis may include, as proof of identity</p>
						<ol>
							<li>a passport or national identity document from the undocumented immigrant’s country of origin that includes the undocumented immigrant’s name and the undocumented immigrant’s photograph or fingerprint;</li>
							<li>the undocumented immigrant’s birth certificate and an identity card that includes the undocumented immigrant’s name and photograph;</li>
							<li>a school identification card that includes the undocumented immigrant’s name and photograph, and school records showing the undocumented immigrant’s name and that the undocumented immigrant is or was enrolled at the school;</li>
							<li>a Uniformed Services identification card issued by the Department of Defense;</li>
							<li>any immigration or other document issued by the United States Government bearing the undocumented immigrant’s name and photograph; or</li>
							<li>a State-issued identification card bearing the undocumented immigrant's name and photograph.</li>
						</ol>
					<h4>Section 2: Age and Income Documentation</h4>
						<p>Documents Establish Age: To establish that an undocumented immigrant meets an age requirement, the undocumented immigrant shall provide proof of identity, as described in the bill, that establishes that the undocumented immigrant is younger than 18 years of age.</p>
						<p>Documents to Establish Income: To establish the undocumented immigrant’s income, the undocumented immigrant shall provide—</p>
						<ol type="a">
							<li>employment records that have been maintained by the Social Security Administration, the Internal Revenue Service, or any other Federal, State, or local government agency;</li>
							<li>bank records; or</li>
							<li>at least 2 sworn affidavits from individuals who are not related to the undocumented immigrant and who have direct knowledge of the undocumented immigrant’s work and income that contain—
								<ol type="i">
									<li>the name, address, and telephone number of the affiant; and</li>
									<li>the nature and duration of the relationship between the affiant and the undocumented immigrant.</li>
								</ol>
							</li>
						</ol>
						<p>Documents To Establish Foster Care, Lack Of Familial Support, Homelessness, Or Serious, Chronic Disability: To establish that the undocumented immigrant was in foster care, lacks parental or familial support, is homeless, or has a serious, chronic disability, the undocumented immigrant shall provide at least 2 sworn affidavits from individuals who are not related to the undocumented immigrant and who have direct knowledge of the circumstances that contain—</p>
						<ol type="a">
							<li>a statement that the undocumented immigrant is in foster care, otherwise lacks any parental or other familiar support, is homeless, or has a serious, chronic disability, as appropriate;</li>
							<li>the name, address, and telephone number of the affiant; and</li>
							<li>the nature and duration of the relationship between the affiant and the undocumented immigrant.</li>
						</ol>
			</div>
			<div class="section" id="A5">
				<h3>Article III: DHS Mandates</h3>
					<h4>Section 1: Prohibition of Dreamer Deportation</h4>
						<p>The Department of Homeland Security (DHS) shall cancel removal and grant lawful permanent resident status on a conditional basis to an undocumented immigrant who is inadmissible or deportable or is in temporary protected status.</p>
						<p>DHS shall cancel the removal of, and adjust to the status of an undocumented immigrant lawfully admitted for permanent residence on a conditional basis, an undocumented immigrant who was granted Deferred Action for Childhood Arrivals (DACA) status unless the undocumented immigrant has engaged in conduct that would make the undocumented immigrant ineligible for DACA.</p>
						<p>DHS may not: (1) grant conditional permanent resident status without the submission of biometric and background data, and completion of background and medical checks; and (2) disclose or use information provided in applications filed under this bill or in DACA requests for immigration enforcement purposes.</p>
						<p>The DHS, in the case of immediate danger may: (1) may terminate a person's conditional permanent resident status, and (2) shall adjust a person's conditional status to permanent resident status.</p>
					<h4>Section 2: DHS Funding</h4>
						<p>In order to carry out this DACA program, the DHS shall receive $350,000,000 per year from the fiscal years 2019 to 2021. This funding shall be appropriated from the crop insurance program, an overfunded program during the Great Depression. It directly subsidizes crops, which was a very big concern in that era due to the fact that a crop failure could put the country in economic ruin. Because our nation is no longer scared of a crop failure, this program’s overfunding is unnecessary. This overfunding kills competition and according to a congressional report, “distorts the insurance market and market for commodity crops by encouraging overplanting and is partially duplicative as other programs provide more rational insurance for farmers.” While some funding must be kept for natural disasters, this program does not need this much funding.</p>
